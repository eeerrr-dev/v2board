-- V2Board native PostgreSQL 18 final-state baseline.
-- This lineage is intentionally independent from the retained MySQL migrations.

-- Installation identity is created by the lifecycle bootstrap, not by this
-- ordinary runtime migration. The local development seed is the sole exception.
CREATE TABLE v2_system_installation (
    singleton SMALLINT PRIMARY KEY CHECK (singleton = 1),
    installation_id UUID NOT NULL UNIQUE,
    lineage TEXT NOT NULL CHECK (lineage IN ('native', 'legacy_migrated')),
    state TEXT NOT NULL CHECK (state IN ('pending', 'active', 'retired')),
    created_at BIGINT NOT NULL,
    activated_at BIGINT,
    source_fingerprint_sha256 TEXT,
    CONSTRAINT chk_system_installation_activation CHECK (
        (state = 'pending' AND activated_at IS NULL)
        OR (state IN ('active', 'retired') AND activated_at IS NOT NULL)
    ),
    CONSTRAINT chk_system_installation_source_fingerprint CHECK (
        source_fingerprint_sha256 IS NULL
        OR source_fingerprint_sha256 ~ '^[0-9a-f]{64}$'
    )
);

CREATE FUNCTION v2_guard_system_installation()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
    IF TG_OP = 'DELETE' THEN
        RAISE EXCEPTION 'installation identity cannot be deleted';
    END IF;
    IF NEW.singleton IS DISTINCT FROM OLD.singleton
       OR NEW.installation_id IS DISTINCT FROM OLD.installation_id
       OR NEW.created_at IS DISTINCT FROM OLD.created_at THEN
        RAISE EXCEPTION 'installation identity cannot be changed';
    END IF;
    IF NEW.lineage IS DISTINCT FROM OLD.lineage
       AND NOT (OLD.lineage = 'legacy_migrated' AND NEW.lineage = 'native') THEN
        RAISE EXCEPTION 'invalid installation lineage transition: % -> %', OLD.lineage, NEW.lineage;
    END IF;
    IF NEW.state IS DISTINCT FROM OLD.state
       AND NOT (
           (OLD.state = 'pending' AND NEW.state = 'active')
           OR (OLD.state = 'active' AND NEW.state = 'retired')
       ) THEN
        RAISE EXCEPTION 'invalid installation state transition: % -> %', OLD.state, NEW.state;
    END IF;
    IF OLD.activated_at IS NOT NULL
       AND NEW.activated_at IS DISTINCT FROM OLD.activated_at THEN
        RAISE EXCEPTION 'installation activation time cannot be changed';
    END IF;
    IF OLD.source_fingerprint_sha256 IS NOT NULL
       AND NEW.source_fingerprint_sha256 IS DISTINCT FROM OLD.source_fingerprint_sha256 THEN
        RAISE EXCEPTION 'installation source fingerprint cannot be changed once recorded';
    END IF;
    RETURN NEW;
END;
$$;

CREATE TRIGGER trg_system_installation_guard
BEFORE UPDATE OR DELETE ON v2_system_installation
FOR EACH ROW EXECUTE FUNCTION v2_guard_system_installation();

