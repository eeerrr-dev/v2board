CREATE TABLE v2_analytics_delivery_batch (
    batch_id UUID PRIMARY KEY,
    event_name TEXT NOT NULL CHECK (btrim(event_name) <> ''),
    schema_major SMALLINT NOT NULL CHECK (schema_major > 0),
    partition_month DATE NOT NULL CHECK (
        partition_month = date_trunc('month', partition_month)::date
    ),
    table_generation INTEGER NOT NULL CHECK (table_generation > 0),
    row_count INTEGER NOT NULL CHECK (row_count BETWEEN 1 AND 100000),
    content_sha256 TEXT NOT NULL CHECK (content_sha256 ~ '^[0-9a-f]{64}$'),
    insert_settings_sha256 TEXT NOT NULL CHECK (insert_settings_sha256 ~ '^[0-9a-f]{64}$'),
    state TEXT NOT NULL CHECK (state IN ('ready', 'publishing', 'published', 'quarantined')),
    lease_owner UUID,
    lease_expires_at BIGINT,
    attempt_count INTEGER NOT NULL CHECK (attempt_count > 0),
    published_at BIGINT,
    quarantined_at BIGINT,
    quarantine_reason TEXT,
    last_error TEXT,
    created_at BIGINT NOT NULL,
    CONSTRAINT chk_analytics_batch_state CHECK (
        (state = 'ready'
            AND lease_owner IS NULL AND lease_expires_at IS NULL
            AND published_at IS NULL AND quarantined_at IS NULL)
        OR (state = 'publishing'
            AND lease_owner IS NOT NULL AND lease_expires_at IS NOT NULL
            AND published_at IS NULL AND quarantined_at IS NULL)
        OR (state = 'published'
            AND lease_owner IS NULL AND lease_expires_at IS NULL
            AND published_at IS NOT NULL AND quarantined_at IS NULL)
        OR (state = 'quarantined'
            AND lease_owner IS NULL AND lease_expires_at IS NULL
            AND published_at IS NULL AND quarantined_at IS NOT NULL
            AND quarantine_reason IS NOT NULL)
    ),
    CONSTRAINT chk_analytics_batch_terminal_time CHECK (
        published_at IS NULL OR quarantined_at IS NULL
    )
);
